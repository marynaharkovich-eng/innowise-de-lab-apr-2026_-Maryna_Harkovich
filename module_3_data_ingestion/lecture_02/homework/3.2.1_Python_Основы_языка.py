# Импорт встроенного модуля math для математических вычислений
import math

# Запрос идентификатора зоны у пользователя через консоль
zone_id = input("Введите идентификатор зоны: ")

# Заранее заданная площадь зоны (в квадратных метрах)
area_total = 225

# Расчет длины одной стороны квадрата (сторона = квадратный корень из площади)
side_length = math.sqrt(area_total)

# Вывод итогового отчета с использованием введенных и рассчитанных данных
print(f"Расчет для объекта: {zone_id}")
print(f"Длина стены зоны: {side_length} метров")

